import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Cluster AI, the helpful assistant for the Cluster trading platform. You help users with:

- **Trading**: Buying/selling stocks and crypto, understanding order types (market, limit, stop-loss), and portfolio management.
- **Automation**: Setting up stock and crypto trading bots with configurable indicators (RSI, MACD, Bollinger Bands, EMA crossovers), stop-loss/take-profit levels, and the self-improving bot feature.
- **Model Building**: Creating ML-powered trading models, selecting tickers, configuring hyperparameters, and training models.
- **Backtesting**: Running historical backtests on models, interpreting results (Sharpe ratio, win rate, max drawdown, CAGR).
- **Community**: Posting, following users, subscribing to published models, and sharing strategies.
- **Platform navigation**: Helping users find features like the AI Bot Builder, Model Builder Hub, Portfolio, Orders, and Settings.

Guidelines:
- Be concise and conversational. Use short paragraphs.
- When explaining trading concepts, keep it beginner-friendly but accurate.
- If you don't know something specific to the user's account, suggest where they can find it in the platform.
- Use markdown formatting (bold, lists, code) when helpful.
- Never provide specific financial advice or guarantee returns. Always remind users that trading involves risk when relevant.
- Be friendly and encouraging.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();

    if (!messages || !Array.isArray(messages)) {
      return new Response(JSON.stringify({ error: "messages array is required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat-assistant error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
